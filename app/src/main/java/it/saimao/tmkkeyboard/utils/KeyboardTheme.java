package it.saimao.tmkkeyboard.utils;

public enum KeyboardTheme {
    DARK("Dark"), MATERIAL_THEME("Material Theme"), SKY_BLUE("Sky Blue"), CYAN("Cyan"), RED_DANGER("Red Danger"),
    LOVELY_PINK("Lovely Pink"), VIOLET("Violet"), SCARLET("Scarlet"), DRACULA("Dracula");
    public final String name;

    KeyboardTheme(String name) {
        this.name = name;
    }
}
